}

add_action('wp_ajax_generate_text', 'debug_generate_text');
add_action('wp_ajax_nopriv_generate_text', 'debug_generate_text');


// функція для перевірки існування терміна
function check_term_exists()
{
    global $wpdb;
    
    $category = sanitize_text_field($_POST['category']);
    $subcategory = sanitize_text_field($_POST['subcategory']);
    $term = sanitize_text_field($_POST['term']);
    
    $table_name = 'glossary';
    $term_exists = $wpdb->get_var($wpdb->prepare(
        "SELECT COUNT(*) FROM $table_name WHERE category = %s AND subcategory = %s AND term = %s",
        $category, $subcategory, $term
    ));

    if ($term_exists)
    {
        wp_send_json_success(array('term_exists' => true));
    }
    else
    {
        wp_send_json_success(array('term_exists' => false));
    }
}

add_action('wp_ajax_check_term_exists', 'check_term_exists');
add_action('wp_ajax_nopriv_check_term_exists', 'check_term_exists');

// функція для додавання нового терміна
function add_new_term() 
{
    check_ajax_referer('text_generation_nonce', 'nonce');
    global $wpdb;

    $categoryNew = sanitize_text_field($_POST['category']);
    $subcategoryNew = sanitize_text_field($_POST['subcategory']);
    $termNew = sanitize_text_field($_POST['term']);
    $definitionNew = sanitize_textarea_field($_POST['definition']);

    // Перевірка наявності значень
    if (empty($categoryNew) || empty($termNew) || empty($definitionNew)) {
        wp_send_json_error('Всі поля є обов’язковими.');
        return;
    }

    $table_name = 'glossary';
    $result = $wpdb->insert($table_name, array(
        'category' => $categoryNew,
        'subcategory' => $subcategoryNew,
        'term' => $termNew,
        'definition' => $definitionNew
    ));

    if ($result) {
        wp_send_json_success();
    } else {
        $error_message = $wpdb->last_error ? $wpdb->last_error : 'Помилка додавання до БД';
        wp_send_json_error($error_message);
    }
}

add_action('wp_ajax_add_new_term', 'add_new_term');
add_action('wp_ajax_nopriv_add_new_term', 'add_new_term');


