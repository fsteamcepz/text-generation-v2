*/
function debug_text_generation()
{
    //error_log('Завантаження скрипта для генерації тексту.');
    
    wp_enqueue_script('text-generator', 
        get_template_directory_uri() . '/assets/js/text-gen.js', 
        array('jquery'), 
        time(), // додаємо час для уникнення кешування під час розробки
        true
    );

    $ajax_data = array(
        'ajax_url' => admin_url('admin-ajax.php'),
        'nonce' => wp_create_nonce('text_generation_nonce'),
        'is_logged_in' => is_user_logged_in() // додаємо статус авторизації
    );
    
    //error_log('Ajax дані: ' . print_r($ajax_data, true));
    wp_localize_script('text-generator', 'textGenAjax', $ajax_data);
}

add_action('wp_enqueue_scripts', 'debug_text_generation');

// функція для генерації тексту
function debug_generate_text()
{
    //error_log('Запит Ajax отримано');
    //error_log('POST дані: ' . print_r($_POST, true));
    
    check_ajax_referer('text_generation_nonce', 'nonce');
    
    $term = sanitize_text_field($_POST['term']);
    error_log('Термін для пошуку: ' . $term);
